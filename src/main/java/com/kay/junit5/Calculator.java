package com.kay.junit5;

/**
 * Calculator
 * @author kay
 * @date 2019/08/27
 */
public class Calculator {
    public int add(int num1, int num2) {
        return num1 + num2;
    }

    public int multiply(int num1, int num2) {
        return num1 * num2;
    }

    public int divide(int num1, int num2) {
        return num1 / num2;
    }

    public int subtract(int num1, int num2) {
        return num1 - num2;
    }
}
